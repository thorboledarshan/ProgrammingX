import java.util.Scanner;

class Logic
{
    void DisplayDigit(int num)
    {
        int iDigit = 0;
        while(num != 0)
        {
            iDigit = num % 10;
            System.out.println(iDigit);
            num = num /10;
        }
      
    }
}


class DisplayDigit19_4
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.DisplayDigit(1234);  
    }
}
