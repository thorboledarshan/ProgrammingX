import java.util.Scanner;

class Logic
{
    void PrintOddNumbers(int num)
    {
        int iCnt = 0;
        for(iCnt = 1; iCnt <= num; iCnt++)
        {
            if(iCnt % 2 != 0)
            {
                System.out.println(iCnt);
            }
        }
       
    }
}


class PrintOddNumbers18_3
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.PrintOddNumbers(10);  
    }
}
