///////////////////////////////////////////////////////////////////////////////////////////////
//
/*  Accept character from user. and display its ascii value in decimal , octal,hexa.
*/
//
///////////////////////////////////////////////////////////////////////////////////////////////


#include<stdio.h>


void DisplayAscii(char cCh)
{
  printf("Decimal   Octal   Hexa \n");
  printf("-------  -------  -----\n");

  printf("%d\t%o\t%X\t",cCh,cCh,cCh);
}

int main()
{
   char cValue = '\0';
   

   printf("Enter Character : ");
   scanf("%c",&cValue);

    DisplayAscii(cValue);
  
    return 0;
}

