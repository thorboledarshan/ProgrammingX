import java.util.Scanner;

class Logic
{
    void DisplayFactors(int n)
    {
        int iCnt = 0;
        for(iCnt = 1; iCnt <= (n / 2); iCnt++)
        {
            if(n % iCnt == 0)
            {
                System.out.println(iCnt);
            }  
        }
    }
    
}


class DisplayFactors21_3
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.DisplayFactors(6);  
    }
}
