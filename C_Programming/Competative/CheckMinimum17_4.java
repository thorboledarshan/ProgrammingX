import java.util.*;

class Logic
{
    void CheckMinimum(int No1, int No2, int No3)
    {
        int iMin = No1;
        if(No2 < iMin)
        {
            iMin = No2;
        }
        else if (No3 < iMin)
        {
            iMin = No3;
        }
        System.out.println("Minimum Number is :"+iMin);
    }
}


class CheckMinimum17_4
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.CheckMinimum(3,7,1);
    }
}

