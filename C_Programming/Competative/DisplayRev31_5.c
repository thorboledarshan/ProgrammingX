#include<stdio.h>

void DisplayRev(char *str , int iSize)
{
  int i = 0;
  for(i = (iSize-1); i>= 0; i-- )
  {
    printf("%c",str[i]);
  }
}

int main()
{
  char Arr[40] = {'\0'};
  int iLength = 0;
  
  printf("Enter String : ");
  scanf("%[^'\n']s",Arr);
  iLength = sizeof(Arr);

  DisplayRev(Arr,iLength);
  return 0;
}

