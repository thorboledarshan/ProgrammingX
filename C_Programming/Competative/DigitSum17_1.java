import java.util.*;

class Logic
{
    void SumOfDigits(int num)
    {
      int iSum = 0;
      int iDigit = 0; 
      int iTemp = num;

      while(num != 0)
      {
        iDigit = num % 10;
        iSum = iSum + iDigit;
        num = num /10;
      }
      System.out.println("Sum of Digit "+iTemp+" is : "+iSum);
    }

}


class DigitSum17_1
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.SumOfDigits(1234);
    }
}

