import java.util.*;

class Logic
{
    void PrintTable(int num)
    {
        int iCnt = 0;
        int iValue = 0;
        for(iCnt = 1 ; iCnt <=10; iCnt++)
        {
            iValue = num * iCnt;
            System.out.println(iValue);
        }   
    }
}


class PrintTable17_5
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.PrintTable(4);
    }
}

