#include<stdio.h>
void ConvertStrToLC(char *str)
{
  while(*str != '\0')
  {
    if(*str >= 'A' && *str <='Z')
    {
      *str = *str + 32;
    }
    printf("%c",*str);
    str++;
  }
  
}

int main()
{
  char Arr[50] = {'\0'};

  printf("Enter String : ");
  scanf("%[^'\n']s",Arr);

  ConvertStrToLC(Arr);
  return 0;
}

