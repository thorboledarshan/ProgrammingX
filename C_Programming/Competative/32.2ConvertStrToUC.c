#include<stdio.h>
void ConvertStrToUC(char *str)
{
  while(*str != '\0')
  {
    if(*str >= 'a' && *str <='z')
    {
      *str = *str - 32;
    }
    printf("%c",*str);
    str++;
  }
  
}

int main()
{
  char Arr[50] = {'\0'};

  printf("Enter String : ");
  scanf("%[^'\n']s",Arr);

  ConvertStrToUC(Arr);
  return 0;
}

