// Q. Accept number from user and display pattern
//
///////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

void DisplayPattern(int n)
{
  int iCnt = 0;
  char iCh = 'A';
  for(iCnt = 1; iCnt <= n; iCnt++)
  {
    printf("%c ",iCh);
    iCh++;
  }
}

int main()
{
   int iValue = 0;
   printf("Enter number of elements : ");
   scanf("%d",&iValue);

   DisplayPattern(iValue);
   return 0;
}
