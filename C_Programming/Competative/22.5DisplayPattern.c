#include<stdio.h>

void DisplayPattern(int iNo)
{
  int iCnt = 0;
  for(iCnt = 0; iCnt < (iNo* 2 ); iCnt = iCnt++)
  {
    iCnt = iCnt + 2;
    printf("%d ", iCnt);

  }
}

int main()
{
   int iValue = 0;
   printf("Enter number of elements : ");
   scanf("%d",&iValue);

   DisplayPattern(iValue);
   return 0;
}
